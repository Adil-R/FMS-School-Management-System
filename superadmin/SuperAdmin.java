    private void btnsalsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalsaveActionPerformed
        // TODO add your handling code here:
        double grosspay;
        
        grosspay = Double.parseDouble(tfgrosspay.getText());
        try {
            int call = NewStaff.setStaff_salary(grosspay);
            if(call == 1)
            {
                JOptionPane.showMessageDialog(null, "Staff Salary details successfylly saved\n","Success", JOptionPane.INFORMATION_MESSAGE);

                //enable the objects of he staff contacts panel
                staffdetailspanel.setEnabled(true);

                tfmob.setEnabled(true);
                tfmob.setText("");
                tfpadd.setEnabled(true);
                tfpadd.setText("");
                tfemail.setEnabled(true);
                tfemail.setText("");
                tftown.setEnabled(true);
                tftown.setText("");
                jbuttoncancelcontacts.setEnabled(true);
                jbuttonsavecontacts.setEnabled(true);

                //disable the staff salary panel
                tfgrosspay.setEnabled(false);
                tfgrosspay.setText("");
                btnsalcancel.setEnabled(false);
                btnsalsave.setEnabled(false);

                refreshForm();
            }

        } catch (SQLException ex) {
            Logger.getLogger(SuperAdmin.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Some unexpected error occured \n" +
                "Please contact the system administrator", "Error "+ex.getErrorCode(), JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnsalsaveActionPerformed

