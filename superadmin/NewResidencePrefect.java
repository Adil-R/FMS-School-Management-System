    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String residencename,post;
        int residenceid,studentid;

        residencename =  cmbresidencename.getSelectedItem().toString();
        post = tfpostname.getText().toUpperCase();
       studentid = Integer.parseInt(jsstudentno.getValue().toString());
        
        if("Select Residence Name".equals(residencename))
        {
            JOptionPane.showMessageDialog(null,
                "Please Select Residence Name to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else if("".equals(post))
        {
            JOptionPane.showMessageDialog(null,
                "Please Enter Post Name to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else if(studentid == 0)
        {
            JOptionPane.showMessageDialog(null,
                "Please Enter Student Number to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            residenceid = QueryResidenceDetails.listofnames.indexOf(residencename);
            

            try {
                
                sqlinserts.NewResidence.setResidenceMonitor(residenceid,studentid, post);
                JOptionPane.showMessageDialog(null, "New Residence Prefect Details Have been Saved \n" +
                    "Press Ok to Continue", "Success", JOptionPane.INFORMATION_MESSAGE);
                btnok.setEnabled(true);
            } catch (SQLException ex) {
                Logger.getLogger(NewResidence.class.getName()).log(Level.SEVERE, null, ex);
                if(ex.getErrorCode() == 1062)
                {
                    JOptionPane.showMessageDialog(null, "The Residence Prefect information  You are Trying to Create Has Already Been Saved Before. \n" +
                        "Press Ok to continue", "Warning "+ex.getErrorCode(), JOptionPane.WARNING_MESSAGE);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Some unexpected error occured \n" +
                        "Please contact the system administrator", "Error ", JOptionPane.ERROR_MESSAGE);
                }
                this.hide();
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

