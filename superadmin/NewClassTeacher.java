    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

            String classname,teachername,cls,tn,todaysdate,postname;
        int classid,staffid;
        
        //get todays date
        todaysdate = selects.QueryMainDetails.getCurrentDate();
        cls = cmbclassname.getSelectedItem().toString();
        classname = cls.toUpperCase();
        tn = cmbstaffname.getSelectedItem().toString();
        teachername = tn.toUpperCase();
        postname = tfpostname.getText().toUpperCase();
        
        if("SELECT CLASS NAME".equals(classname))
        {
            JOptionPane.showMessageDialog(null,
                        "Please Select Class Name to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else if("SELECT MEMBER OF STAFF".equals(teachername))
        {
            JOptionPane.showMessageDialog(null,
                        "Please Select Class Teacher to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else if("".equals(postname))
        {
            JOptionPane.showMessageDialog(null,
                        "Please Select Teachers Position Name to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            int staffd = QueryStafftDetails.listofnames.indexOf(tn);
            int classd = QueryClassDetails.listofnames.indexOf(cls);
            staffid = Integer.parseInt(QueryStafftDetails.listofids.get(staffd).toString());
            classid = Integer.parseInt(QueryClassDetails.listofids.get(classd).toString());
            
            try {
                sqlinserts.NewClass.newClassTeacher(classid, staffid, postname);
                JOptionPane.showMessageDialog(null, "New Class Teacher Has been Saved \n" +
                        "Press Ok to Continue", "Success", JOptionPane.INFORMATION_MESSAGE);
                
                if(caller == 1)
                {
                    btncontinue.setEnabled(true);
                    btnok.setEnabled(true);
                }
                
            } 
            catch (SQLException ex) {
                Logger.getLogger(NewClassTeacher.class.getName()).log(Level.SEVERE, null, ex);
                
                if(ex.getErrorCode() == 1062)
                {
                JOptionPane.showMessageDialog(null, "The Class Teacher information  You are Trying to Create Has Already Been Saved Before. \n" +
                        "Press Ok to continue", "Warning "+ex.getErrorCode(), JOptionPane.WARNING_MESSAGE);
                }
                else
                {
                JOptionPane.showMessageDialog(null, "Some unexpected error occured \n" +
                        "Please contact the system administrator", "Error ", JOptionPane.ERROR_MESSAGE);
                }
                this.hide();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

