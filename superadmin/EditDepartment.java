    private void jbtsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtsaveActionPerformed
        String deptname,depthead,description;
        int staffid;
        deptname = tfdeptname.getText().toUpperCase();
        depthead = cmbdepthead.getSelectedItem().toString().toUpperCase();

        int staffidid = QueryStafftDetails.listofnames.indexOf(depthead);
        staffid = Integer.parseInt(QueryStafftDetails.listofids.get(staffidid).toString());
        description = jtadescription.getText().toUpperCase();

        if("".equals(deptname))
        {
            JOptionPane.showMessageDialog(null,
                "Please Enter Department Name to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else if(cmbdepthead.getSelectedIndex() == 0)
        {
            JOptionPane.showMessageDialog(null,
                "Please Select Department Head to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            try {
                //create department
                sqlupdates.DepartmentInfo.updateDepartment(deptname, description, deptid);
                //create department head
                sqlupdates.DepartmentInfo.updateDepartmentHead(staffid, deptid);
                JOptionPane.showMessageDialog(null, "Department Details Have been Updated\n" +
                    "Press Ok to Continue", "Success", JOptionPane.INFORMATION_MESSAGE);
                btnok.setEnabled(true);

            } catch (SQLException ex) {
                Logger.getLogger(EditDepartment.class.getName()).log(Level.SEVERE, null, ex);
                if(ex.getErrorCode()==1062)
                {
                    JOptionPane.showMessageDialog(null, "Department already Edited \n "
                        , "Error "+ex.getErrorCode()+ "Occured", JOptionPane.WARNING_MESSAGE);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Unexpected Error Occured \n" +
                        "Please Contact the System Administrator", "Error "+ex.getErrorCode(), JOptionPane.ERROR_MESSAGE);
                    this.hide();
                }

            }
        }
    }//GEN-LAST:event_jbtsaveActionPerformed

    private void cmbdeptselectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbdeptselectActionPerformed
        // TODO add your handling code here:
        
        int selected = cmbdeptselect.getSelectedIndex();
        deptid = deptids.get(selected);
        try
        {
        ArrayList<String> deptdetails = selects.QueryDepartmenDetails.getDepartmentDetails(deptid);
        int hodid = selects.QueryDepartmenDetails.getDepartmentHeadID(deptid);
        
        //initialize staff details
        ArrayList<String> staffnames = selects.QueryStafftDetails.getStaffNames();
        int setselectedstaff = selects.QueryStafftDetails.listofids.indexOf(hodid);
        
        
        String deptname,deptdescription;
        deptname = deptdetails.get(0);
        deptdescription = deptdetails.get(1);
        
        cmbdepthead.setModel(new DefaultComboBoxModel(QueryStafftDetails.listofnames.toArray(new String[QueryStafftDetails.listofnames.size()])));
        tfdeptname.setText(deptname);
        jtadescription.setText(deptdescription);
        cmbdepthead.setSelectedIndex(setselectedstaff);
        
        
        }
        catch(SQLException ex)
        {
             JOptionPane.showMessageDialog(null, "Department Details not Found \n" +
                        "", "Unexpected Error"+ex.getErrorCode(), JOptionPane.ERROR_MESSAGE);
        }
        
        
        
    }//GEN-LAST:event_cmbdeptselectActionPerformed

