    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        fordetails = cmbbordingdetails.getSelectedItem().toString().toUpperCase();
        classreq = cmbclassname1.getSelectedItem().toString();
        if("SELECT".equals(fordetails))
        {
            JOptionPane.showMessageDialog(null,
                "Please Select Boarding Details to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else if(jrterm1.isSelected() == false && jrterm2.isSelected() == false && jrterm3.isSelected() == false)
        {
            JOptionPane.showMessageDialog(null,
                "Please Select Term to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        //enter the other check details here
        else if(cmbclassname1.getSelectedIndex()==0)
        {
            JOptionPane.showMessageDialog(null,
                "Please Select a Class Name to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
         termid = selects.QueryTermDetails.getTermID(termname, year);
         clsid = QueryClassDetails.listofnames.indexOf(classreq);
         loadedfee = selects.QueryMealsDetails.getMealFee(termid, clsid, fordetails);
         if(!"".equals(loadedfee))
         {
         lblfee.setVisible(true);
         txtfee.setVisible(true);
         txtfee.setText(loadedfee);
         }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

