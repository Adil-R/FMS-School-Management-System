    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String fee;
        fee = txtfee.getText();
         if("".equals(txtfee.getText()))
        {
            JOptionPane.showMessageDialog(null,
                "Please Enter Fee Ammount to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            int clsidid = QueryClassDetails.listofnames.indexOf(classreq);
            clsid = Integer.parseInt(QueryClassDetails.listofids.get(clsidid).toString());
            
            termid = selects.QueryTermDetails.getTermID(termname, year);
            try {
                sqlupdates.MealsInfo.updateMealsFee(Double.parseDouble(fee), termid, clsid, fordetails);
                lblconfirm.setText("Updated");

            } catch (SQLException ex) {
                Logger.getLogger(NewTransport.class.getName()).log(Level.SEVERE, null, ex);
                if(ex.getErrorCode() == 1062)
                {
                    JOptionPane.showMessageDialog(null, "The Meals Fee Details You are Trying to Create Have Already Been Saved Before. \n" +
                        "Press Ok to continue", "Warning "+ex.getErrorCode(), JOptionPane.WARNING_MESSAGE);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Some unexpected error occured \n" +
                        "Please contact the system administrator", "Error ", JOptionPane.ERROR_MESSAGE);
                    this.hide();
                }

            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

