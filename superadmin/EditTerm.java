    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
     //check whether select form is filled correctly
       if(cmbtermnameselect.getSelectedIndex() == 0)
       {
           JOptionPane.showMessageDialog(null, "Please Select Term Name to Continue. \n" +
                        "", "Warning", JOptionPane.WARNING_MESSAGE);
       }
       else if("    ".equals(tfyear.getText()))
       {
            JOptionPane.showMessageDialog(null, "Please Enter Year to Continue. \n" +
                        "", "Warning", JOptionPane.WARNING_MESSAGE);
       }
       else{
        
     //get data for the main panel;
        String yearx,termnamex;
        int yr;
        boolean error = false;
        java.util.Date strdt,clsdt;
        yearx = tfyear.getText();
        termnamex = cmbtermnameselect.getSelectedItem().toString().toUpperCase();
        try{
            yr = Integer.parseInt(yearx);
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Please Enter Correct Year to Continue. \n" +
                        "", "Warning", JOptionPane.WARNING_MESSAGE);
            error = true;
        }
        if(error!=true)
        {
        try
        {
        details = selects.QueryTermDetails.getTermDetails(termnamex, yearx);
        // inialize main panels data
        String name, startdatestr, closedatestr, remarks;
        name = details.get(0).toString();
        startdatestr = details.get(1).toString();
        closedatestr = details.get(2).toString();
        remarks = details.get(3).toString();
        cmbtermname.setSelectedItem(name);
         try {
            SimpleDateFormat format = new SimpleDateFormat("yyyy-mm-dd");
            strdt = format.parse(startdatestr);
            jstartdate.setDate(strdt);
        } catch (ParseException ex) {
            Logger.getLogger(EditStaff.class.getName()).log(Level.SEVERE, null, ex);
        }
          try {
            SimpleDateFormat format = new SimpleDateFormat("yyyy-mm-dd");
            clsdt = format.parse(closedatestr);
            jclosedate.setDate(clsdt);
        } catch (ParseException ex) {
            Logger.getLogger(EditStaff.class.getName()).log(Level.SEVERE, null, ex);
        }
        jtaremarks.setText(remarks);
        }
        catch(Exception ex)
        {
            error = true;
        }
        if(error!=true)
        {
     //enable the main panel
       cmbtermname.setEnabled(true);
       jstartdate.setEnabled(true);
       jclosedate.setEnabled(true);
       jtaremarks.setEnabled(true);
       jbtsave.setEnabled(true);
       
     //didable the select panel
        cmbtermnameselect.setEnabled(false);
        tfyear.setEnabled(false);
        btnedit.setEnabled(false);
       }
      }
        
       }
    }//GEN-LAST:event_btneditActionPerformed

