    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
       
       
       try {
                sqlinserts.NewStudentLeader.createNewStudentLeader(postname,registrationnumber, role);
                 JOptionPane.showMessageDialog(null, "New Student Leader Details have been Saved \n" +
                        "Press Ok to Continue", "Success", JOptionPane.INFORMATION_MESSAGE);
                 this.hide();
            } catch (SQLException ex) {
                Logger.getLogger(StudentLeaderDetails.class.getName()).log(Level.SEVERE, null, ex);
                
                 if(ex.getErrorCode() == 1062)
                {
                JOptionPane.showMessageDialog(null, "The Student Leader Information Has Already Been Saved Before. \n" +
                        "Press Ok to continue", "Warning "+ex.getErrorCode(), JOptionPane.WARNING_MESSAGE);
                }
                else
                {
                JOptionPane.showMessageDialog(null, "Some unexpected error occured \n" +
                        "Please contact the system administrator", "Error ", JOptionPane.ERROR_MESSAGE);
                }
                this.hide();
            }
    }//GEN-LAST:event_jButton3ActionPerformed

