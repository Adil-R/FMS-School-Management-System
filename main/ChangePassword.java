    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
          //check to ensure user enters all data
          if("".equals(tfoldpass.getText()))
          {
              JOptionPane.showMessageDialog(null,
                        "Please Enter Old Password to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
          }
          else if("".equals(tfnewpass.getText()))
          {
              JOptionPane.showMessageDialog(null,
                        "Please Enter New Password to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
          }
          else if("".equals(tfnewpassconfirm.getText()))
          {
              JOptionPane.showMessageDialog(null,
                        "Please Cofirm New Password to Continue\n", "Warning",JOptionPane.WARNING_MESSAGE);
          }
          else if(!tfnewpass.getText().equals(tfnewpassconfirm.getText()))
          {
              JOptionPane.showMessageDialog(null,
                        "New Password Doesnt match Confirmation. Please Check to contine\n", "Warning",JOptionPane.WARNING_MESSAGE);
          }
          else
          {
              //get hash from the user inputs
              useroldpass = selects.QueryMainDetails.SHA256(this.tfoldpass.getText());
              usernewpass = selects.QueryMainDetails.SHA256(this.tfnewpass.getText());
              userconfirmpass = selects.QueryMainDetails.SHA256(this.tfnewpassconfirm.getText());
              String userhint =  tfhint.getText();
              
              if(useroldpass.equals(olddpassword))
              {
                  try {
                      sqlupdates.LoginInfo.updatePassword(nationalid, usernewpass, userhint);
                      JOptionPane.showMessageDialog(null,
                            "Your Password has been changed successfully\n", "Success",JOptionPane.WARNING_MESSAGE);
                      this.dispose();
                  } catch (SQLException ex) {
                     JOptionPane.showMessageDialog(null, "Some unexpected error occured \n" +
                        "Please contact the system administrator", "Error ", JOptionPane.ERROR_MESSAGE);
                     this.dispose();
                  }
              }
              else
              {
                  JOptionPane.showMessageDialog(null,
                        "Password was not Changed.\nYou entered a Wrong Password\nEnter Correct Password and Try Again", "Wrong Password",JOptionPane.ERROR_MESSAGE);
              this.dispose();
              }
              
          }
        
    }//GEN-LAST:event_btnsaveActionPerformed

