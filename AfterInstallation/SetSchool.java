    private InputStream readDefaultSchoolLogo()
    {
        DBConnection dbconn =new DBConnection();
        Connection conn = dbconn.vconnection;
        PreparedStatement pstmt;
        String sql = "SELECT `logo` FROM `default_school_logo` WHERE `ID`=1";
        InputStream in = null;
        
        try {
            pstmt = conn.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();
            
            rs.next();
            in = rs.getBinaryStream("logo"); 
             
        } catch (SQLException ex) {
            Logger.getLogger(InsertSchoolLogo.class.getName()).log(Level.SEVERE, null, ex);
        }
        return in;
    }
   
    
    
