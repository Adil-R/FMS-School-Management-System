    private void jbuttoncancelcontactsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttoncancelcontactsActionPerformed
        // TODO add your handling code here:
        if("".equals(tfpass.getText()))
        {
                JOptionPane.showMessageDialog(null,
                "Please Enter Password to Continue", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else if("".equals(tfconfirmpass.getText()))
        {
                JOptionPane.showMessageDialog(null,
                "Please Confirm Password Continue", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else if(!tfpass.getText().equals(tfconfirmpass.getText()))
        {
            JOptionPane.showMessageDialog(null,
                "Please Enter the Mobile Phone Number to Continue", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else if("".equals(tfhint.getText()))
        {
            JOptionPane.showMessageDialog(null,
                "Please Enter the Mobile Phone Number to Continue", "Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            String nationalid,password,hint,level;
            nationalid = this.nationalid;
            password = QueryMainDetails.SHA256(tfpass.getText());
            hint = tfhint.getText();
            level = tflevel.getText().toUpperCase();
            try {
                sqlinserts.NewLogin.createNewUser(nationalid, password, hint, "1");
                 JOptionPane.showMessageDialog(null, "Login Details Successfylly Saved\n","Success", JOptionPane.INFORMATION_MESSAGE);
                this.lblsaved.setText("Details Saved");
            } catch (SQLException ex) {
                Logger.getLogger(SetPassWord.class.getName()).log(Level.SEVERE, null, ex);
                  if(ex.getErrorCode() == 1062)
                {
                    JOptionPane.showMessageDialog(null, "The Login Information   Has Already Been Saved Before. \n" +
                        "Press Ok to continue", "Warning "+ex.getErrorCode(), JOptionPane.WARNING_MESSAGE);
                }
                else
                JOptionPane.showMessageDialog(null, "Some unexpected error occured \n" +
                    "Please contact the system administrator", "Error "+ex.getErrorCode(), JOptionPane.WARNING_MESSAGE);
            }
            
                    
        }
    }//GEN-LAST:event_jbuttoncancelcontactsActionPerformed

